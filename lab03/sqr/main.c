#include <stdio.h>
int main() {
    // Оголошення змінних
    float a, area, perimeter;
    // Введення довжини сторони квадрата
    printf("Введіть довжину сторони квадрата (a): ");
    scanf("%f", &a);
    // Обчислення площі квадрата
    area = a * a;
    // Обчислення периметра квадрата
    perimeter = 4 * a;
    // Виведення результатів
    printf("Площа квадрата: %.2f\n", area);
    printf("Периметр квадрата: %.2f\n", perimeter);
    return 0;
}
